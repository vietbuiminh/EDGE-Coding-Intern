            It did not take me long to notice how few women were in this industry and how differently men and women can function at work. I loved my job, but I was really starting to feel like an island. I knew I had to change that, and I love a good challenge, thus the journey to launching H<sup>er</sup><sub>2</sub>O&trade; began!<br><br>

            Over the past several years I have launched smaller women in water and sanitation efforts and assisted others to launch efforts around the U.S. But I knew I wanted to do more, and I just could not get the idea of an international organization out of my head. In 2019, I was invited to attend the United Nations (UN) Commission on the Status of Women, where I met women in water and sanitation from around the world. I learned more, and participated in, high-level conversations with other water professionals, UN Delegates, academics, and Parliamentarians about the challenges of women in water and sanitation globally and current efforts to empower them.<br><br>

            Leading up to the event, I was introduced to the Executive Director of the Tanzanian Gender Networking Program (TGNP). I learned about the women-led groups that are designing and implementing water and sanitation solutions throughout Tanzania and the extreme challenges they faced in doing so. There it was again, that relentless feeling that I had to do something to help. I thought to myself, if these women can walk miles a day to fetch water, in worse climate conditions with less adequate equipment, I then had to challenge myself in the same way. In that moment I decided to trek Mt. Kilimanjaro to raise money for TGNP’s women-led water and sanitation projects. In September 2019, despite a nasty bout of altitude sickness, I reached Uhuru Peak, raising $3,000 for TGNP!<br><br>
            
            Upon my return from Tanzania, the glow of summitting Mt. Kilimanjaro still fresh and clearly visible upon me, I received an unexpected call. The caller was inviting me to present in the Women Leaders track at a water and waste management conference in India. At this point, I was completely convinced that women in water and sanitation is my one true purpose in life. H<sup>er</sup><sub>2</sub>O&trade; was legally founded on January 24, 2020 and just 14 days later I landed in Delhi.<br><br>

             This journey that has led me to found H<sup>er</sup><sub>2</sub>O&trade; has been nothing short of thrilling, and I cannot wait to see where this journey takes me next. We are wather™ and I invite you to join us to build the future of water, one woman at a time!</p>
